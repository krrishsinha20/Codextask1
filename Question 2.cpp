#include <iostream>
using namespace std;

int main() {
    int a;
    short int b;
    long int c;
    double d;
    cout<<"Integer : "<<sizeof(a)<<endl;
    cout<<"Short integer : "<<sizeof(b)<<endl;
    cout<<"Long integer : "<<sizeof(c)<<endl;
    cout<<"Double : "<<sizeof(d)<<endl; 
}